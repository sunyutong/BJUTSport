package com.bjutsport.player;


public class basketballPlayer {

    //姓名
    private String name;
    //年龄
    private int age;
    //性别
    private boolean sex;

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public void setSex(boolean sex) {
        this.sex = sex;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public boolean getSex() {
        return sex;
    }
}
